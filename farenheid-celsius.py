fahrenheit = float(input("Ingrese los grados Fahrenheit: "))
celsius = (fahrenheit - 32) * 5/9
print("{} grados Fahrenheit son {} grados Celsius.".format(fahrenheit, celsius))


# En este programa se pide al usuario que ingrese los grados Fahrenheit utilizando el método input(). Luego, se utiliza la fórmula de conversión para calcular los grados Celsius y se almacena en la variable celsius. Por último, se utiliza el método format() para imprimir el resultado de la conversión en una cadena legible para el usuario.

// Concatenar cadenas:

let x1 = "Hola";
let y = " mundo";
let result = x1 + y;
console.log(result); // imprime "Hola mundo"

// Repetir una cadena:

let x2 = "Hola";
result = x2.repeat(3);
console.log(result); // imprime "HolaHolaHola"

// Acceder a un carácter específico de una cadena:

let x3 = "Hola mundo";
result = x3.charAt(4);
console.log(result); // imprime "a"

// Acceder a un rango de caracteres de una cadena:

let x4 = "Hola mundo";
result = x4.substring(4, 8);
console.log(result); // imprime "a mu"

// Longitud de una cadena:

let x5 = "Hola mundo";
result = x5.length;
console.log(result); // imprime 10

// Reemplazar una subcadena en una cadena:

let x6 = "Hola mundo";
result = x6.replace("mundo", "JavaScript");
console.log(result); // imprime "Hola JavaScript"

// Dividir una cadena en una lista de subcadenas:

let x7 = "Hola mundo, bienvenido a JavaScript";
result = x7.split(",");
console.log(result); // imprime ["Hola mundo", " bienvenido a JavaScript"]

// Convertir una cadena a mayúsculas o minúsculas:

let x8 = "Hola mundo";
result = x8.toUpperCase();
console.log(result); // imprime "HOLA MUNDO"

let x9 = "HOLA MUNDO";
result = x9.toLowerCase();
console.log(result); // imprime "hola mundo"

// Concatenar una cadena con un formato específico:

let x10 = "Hola";
let y1 = " mundo";
result = `${x10} ${y1}`;
console.log(result); // imprime "Hola mundo"








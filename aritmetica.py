# unos ejemplos de aritmetica con python

# Aquí hay algunos ejemplos de operaciones aritméticas básicas en Python:

# Suma:

import math
x = 5
y = 3
result = x + y
print(result)  # imprime 8

# Resta:

x = 5
y = 3
result = x - y
print(result)  # imprime 2

# Multiplicación:

x = 5
y = 3
result = x * y
print(result)  # imprime 15

# División:

x = 5
y = 3
result = x / y
print(result)  # imprime 1.6666666666666667

# Exponenciación:

x = 5
y = 3
result = x ** y
print(result)  # imprime 125

# División entera:

x = 5
y = 3
result = x // y
print(result)  # imprime 1

# Módulo:

x = 5
y = 3
result = x % y
print(result)  # imprime 2

# Es importante tener en cuenta que Python también admite operaciones aritméticas con números flotantes y complejos.

# Para raiz cudrada y cubica

# Para calcular la raíz cuadrada y cúbica en Python, se pueden usar las funciones sqrt() y pow() de la biblioteca matemática "math".

# Raíz cuadrada:

x = 9
result = math.sqrt(x)
print(result)  # imprime 3.0

# Raíz cúbica:

x = 8
result = math.pow(x, 1/3)
print(result)  # imprime 2.0

# También se puede usar operadores especiales(**) para calcular raíces con exponentes fraccionarios.

# Raíz cuadrada:

x = 9
result = x ** 0.5
print(result)  # imprime 3.0

# Raíz cúbica:

x = 8
result = x ** (1/3)
print(result)  # imprime 2.0

# Es importante tener en cuenta que las funciones sqrt() y pow() solo funcionan con números positivos. Para números negativos, se pueden usar números complejos.

# unos ejemplos de aritmetica con python

# Aquí hay algunos ejemplos de operaciones aritméticas básicas en Python:

# Suma:

x = 5
y = 3
result = x + y
print(result)  # imprime 8

# Resta:

x = 5
y = 3
result = x - y
print(result)  # imprime 2

# Multiplicación:

x = 5
y = 3
result = x * y
print(result)  # imprime 15

# División:

x = 5
y = 3
result = x / y
print(result)  # imprime 1.6666666666666667

# Exponenciación:

x = 5
y = 3
result = x ** y
print(result)  # imprime 125

# División entera:

x = 5
y = 3
result = x // y
print(result)  # imprime 1

# Módulo:

x = 5
y = 3
result = x % y
print(result)  # imprime 2

# Es importante tener en cuenta que Python también admite operaciones aritméticas con números flotantes y complejos.
